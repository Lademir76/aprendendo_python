# 🍋 Lista de frutas
frutas = ["limao","laranja", "uva"]

# 📘 String representando um curso
curso = "Curso de python"

# ❌ Verifica se "laranja" NÃO está na lista de frutas
print("laranja" not in frutas)  # True — "laranja" não está na lista

# ✅ Verifica se "limao" está na lista de frutas
print("limao" in frutas)        # True — "limao" está na lista

# 🔍 Verifica se "Python" está na string do curso (atenção à capitalização!)
print("Python" in curso)        # False — "Python" com P maiúsculo não está presente (case-sensitive)

# 🧠 O que são operadores de identidade?
#Os operadores de identidade em Python são usados para verificar se duas variáveis apontam para o mesmo objeto na memória.