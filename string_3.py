nome = "Lademir Salomao Bechara"

print(nome[0])         # Imprime o primeiro caractere: 'L'
print(nome[-2])        # Imprime o penúltimo caractere: 'r'
print(nome[:9])        # Imprime do início até o índice 8: 'Lademir S'
print(nome[10:])       # Imprime do índice 10 até o final: 'alomao Bechara'
print(nome[10:16])     # Imprime do índice 10 até 15: 'alomao'
print(nome[10:16:2])   # Imprime do índice 10 até 15, pulando de 2 em 2: 'aoo'
print(nome[:])         # Imprime a string inteira: 'Lademir Salomao Bechara'
print(nome[::-1])      # Imprime a string invertida: 'arahceB oamolaS rimedaL'
